#include <string.h>

char	*lu_strncat(char *s1, char *s2, size_t n)
{
	size_t		i;

	i = 0;
	while (s1[i])
		i++;
	while (n > 0 && *s2)
	{
		s1[i] = *s2;
		i++;
		s2++;
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
