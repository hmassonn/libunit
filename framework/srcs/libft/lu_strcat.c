#include <string.h>

char	*lu_strcat(char *s1, char *s2)
{
	size_t		i;

	i = 0;
	while (s1[i])
		i++;
	while (*s2)
	{
		s1[i] = *s2;
		i++;
		s2++;
	}
	s1[i] = '\0';
	return (s1);
}
