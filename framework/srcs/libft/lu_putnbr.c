#include "lu_libft.h"

void	lu_putnbr(int n)
{
	if (n >= 0)
	{
		if (n >= 10)
			lu_putnbr(n / 10);
		lu_putchar(n % 10 + '0');
	}
	else
	{
		lu_putchar('-');
		if (n <= -10)
			lu_putnbr(n / -10);
		lu_putchar(n % -10 * -1 + '0');
	}
}
