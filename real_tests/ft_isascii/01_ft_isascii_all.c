#include "launcher.h"

int		ft_isascii_all(void)
{
	int	i;

	i = -127;
	while (i <= 255)
	{
		if ((isascii(i) != 0) != (ft_isascii(i) != 0))
			return (-1);
		i++;
	}
	return (0);
}
