#include "launcher.h"

int			ft_isalpha_all_test(void)
{
	int i;

	i = -1267;
	while (i <= 255)
	{
		if ((isalpha(i) == 0) != (ft_isalpha(i) == 0))
			return (-1);
		i++;
	}
	return (0);
}
