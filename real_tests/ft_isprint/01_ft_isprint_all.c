#include "launcher.h"

int		ft_isprint_all(void)
{
	int	i;

	i = -127;
	while (i < 255)
	{
		if ((isprint(i) != 0) != (ft_isprint(i) != 0))
			return (-1);
		i++;
	}
	return (0);
}
