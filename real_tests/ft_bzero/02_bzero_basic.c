#include "launcher.h"

int		bzero_basic(void)
{
	char t[255];

	t[42] = 0;
	t[43] = 1;
	t[254] = 5;
	ft_bzero((char*)t, 255);
	if (t[43] == 0 && t[254] == 0)
		return (0);
	return (-1);
}
